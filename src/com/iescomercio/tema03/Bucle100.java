package com.iescomercio.tema03;

/**
 * Mostra los 100 primeros números naturales
 * 
 */
public class Bucle100 {
    public static void main(String[] args) {
        int num=1;
        
        while(num<=100) {
            System.out.println("Número: "+num);
            num=num+1;
        }
    }
}
