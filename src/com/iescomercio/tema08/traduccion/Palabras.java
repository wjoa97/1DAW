package com.iescomercio.tema08.traduccion;

/**
 *
 * 
 */
public class Palabras {
    private String ingles, frances;

    public Palabras(String ingles, String frances) {
        this.ingles = ingles;
        this.frances = frances;
    }
    public String getIngles() {
        return ingles;
    }

    public void setIngles(String ingles) {
        this.ingles = ingles;
    }

    public String getFrances() {
        return frances;
    }

    public void setFrances(String frances) {
        this.frances = frances;
    }
    
    
}
